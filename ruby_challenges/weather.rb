weather = "sunny"

case weather
when 'sunny'
  puts "How about a sun dress?"
when 'cloudy'
  puts "Maybe take a jacket!"
when 'foggy'
  puts "Jeans are always a good call."
when 'rainy'
  puts "Definatly take an umbrella."
when 'snowy'
  puts "Pull your snow pants out and make a snowman!"
else
  puts "Keep your head up. Don't let it afect your mood."
end
